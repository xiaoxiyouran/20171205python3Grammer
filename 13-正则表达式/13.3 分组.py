import re
# () 带括号的是一组
m = re.match(r'^(\d{3})-(\d{3,8})$', '010-12345')
print(m.group(0))   # 永远是原字符串

print(m.group(1))

print(m.group(2))

# 010-12345 #
# 010
# 12345

# 识别合法的时间
t = '19:05:30'
m = re.match(r'^(0[0-9]|1[0-9]|2[0-3]|[0-9])\:(0[0-9]|1[0-9]|2[0-9]|3[0-9]|4[0-9]|5[0-9]|[0-9])\:(0[0-9]|1[0-9]|2[0-9]|3[0-9]|4[0-9]|5[0-9]|[0-9])$', t)
print( m.groups() )
# ('19', '05', '30')

data = '12-05'
m = re.match(r'^(0[1-9]|1[0-2]|[0-9])-(0[1-9]|1[0-9]|2[0-9]|3[0-1]|[0-9])$',data)
print( m.groups() )

