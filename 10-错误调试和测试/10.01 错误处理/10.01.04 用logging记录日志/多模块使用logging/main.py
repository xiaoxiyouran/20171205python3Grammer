import logging
import logging.config

logging.config.fileConfig('logging.conf')
root_logger = logging.getLogger('root')
root_logger.debug('test root logger...')

logger = logging.getLogger('main')
logger.info('test main logger')
logger.info('start import module \'mod\'...')
import mod

logger.debug('let\'s test mod.testLogger()')
mod.testLogger()

root_logger.info('finish test...')

# 控制台的输出
# 2017-12-07 13:32:58,589 - root - DEBUG - test root logger...
# 2017-12-07 13:32:58,589 - main - INFO - test main logger
# 2017-12-07 13:32:58,589 - main - INFO - start import module 'mod'...
# 2017-12-07 13:32:58,594 - main.mod.submod - INFO - logger of submod say something...
# 2017-12-07 13:32:58,595 - main.mod - INFO - logger of mod say something...
# 2017-12-07 13:32:58,595 - main - DEBUG - let's test mod.testLogger()
# 2017-12-07 13:32:58,595 - main.mod - DEBUG - this is mod.testLogger...
# 2017-12-07 13:32:58,595 - main.mod.submod - INFO - this is submod.tst()...
# 2017-12-07 13:32:58,595 - root - INFO - finish test...

